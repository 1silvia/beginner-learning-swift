//switch statement - similar to 'if statements'

//You need to have at least 10 types of fruits in your basket. Print 'not enough' if you have less than 10, or print "no need to buy more" if you have at least 10.

var fruits = 10

switch fruits {
case 5:
    print("not enough")
case 10:
    print("no need to buy more")
default:
    print("no need to count")
    
}
